-- ---------------------------------------------------------------------------
-- Verify
-- ---------------------------------------------------------------------------
SELECT n.slug, n.title, n.published, n.cover_url, count(s.id) AS scenes
FROM novels n
LEFT JOIN scenes s ON s.novel_id = n.id
GROUP BY n.id
ORDER BY n.created_at;
